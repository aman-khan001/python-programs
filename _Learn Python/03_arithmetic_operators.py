''' Arithmetic operations are used to perform mathematical calculations on numeric values.'''

# Declaring two numeric variables
a = 10
b = 3

# Performing arithmetic operations and printing the results
print("Addition:", a + b)        # 10 + 3 = 13
print("Subtraction:", a - b)     # 10 - 3 = 7
print("Multiplication:", a * b)  # 10 * 3 = 30
print("Division:", a / b)        # 10 / 3 = 3.333...
print("Floor Division:", a // b) # 10 // 3 = 3 (removes decimal part)
print("Modulus:", a % b)         # 10 % 3 = 1 (remainder)
print("Exponentiation:", a ** b) # 10 ** 3 = 1000 (10 raised to the power of 3)
