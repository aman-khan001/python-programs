'''
  Variables are the container, used to store data values.
  Variables can hold different types of data, such as strings, integers, floats, and booleans.
'''

# Declaring variables and assigning values
name = "Lucifer"       # String variable
age = 25               # Integer variable
height = 5.9           # Float variable
is_student = True      # Boolean variable

# Displaying the values of variables using print()
print("Name:", name)          # Prints: Name: Lucifer
print("Age:", age)            # Prints: Age: 25
print("Height:", height)      # Prints: Height: 5.9
print("Student:", is_student) # Prints: Student: True
