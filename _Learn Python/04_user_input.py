# 🐣 Program: Taking User Input
# This program shows how to take input from the user and display it back

''' The input() function is used to get input from the user.'''

# Input from the user is taken as a string by default

name = input("Enter your name: ")       # Takes string input
age = input("Enter your age: ")         # Takes input as string by default

# Displaying the values entered by the user
print("Hello,", name + "!")               # Greets the user by name
print("You are", age, "years old.")      # Displays the entered age
