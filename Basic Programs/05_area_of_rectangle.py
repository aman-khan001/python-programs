# This program defines a function to calculate the area of a rectangle.

'''
    The area_of_rectangle function takes the length and width of a rectangle as inputs
    and returns the area using the formula: Area = length Ã— width.
'''

def area_of_rectangle(length, width):
    return length * width

# Example usage
print("Area of rectangle with length 10 and width 5:", area_of_rectangle(10, 5))  # Output: 50
print("Area of rectangle with length 7 and width 3:", area_of_rectangle(7, 3))    # Output: 21
