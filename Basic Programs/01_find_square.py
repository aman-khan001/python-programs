# This program defines a function to find the square of a number.

'''
    The square function takes a number as input and returns its square.
    It multiplies the number by itself to compute the square.
'''

def square(num):
    return num * num

# Example usage

print("Square of 5:", square(5))  # Output: 25
print("Square of 10:", square(10))  # Output: 100

