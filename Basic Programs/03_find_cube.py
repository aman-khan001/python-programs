# This program defines a function to find the cube of a number.

'''
    The cube function takes a number as input and returns its cube.
    It raises the number to the power of 3 to compute the cube.
'''

def cube(n):
    """Returns the cube of a number."""
    return n ** 3

# Example usage
print("Cube of 3:", cube(3))  # Output: 27
print("Cube of 4:", cube(4))  # Output: 64
print("Cube of 5:", cube(5))  # Output: 125
